const Max = require('./Max');

describe('Max', () => {

    it('Max of [2,1,5,4,3]', () => {
        expect(Max([2,1,5,4,3])).toEqual(5);
    });

    it('Max of [[1,2],[78,100],[45,21]]', () => {
        expect(Max([[1,2],[78,100],[45,21]])).toEqual(100);
    });

    it('Max of [[[[100,45],1000,1789],2000],178]', () => {
        expect(Max([[[[100,45],1000,1789],2000],178])).toEqual(2000);
    });

    it('Max of [[[[100,45],[5000],[1789]],[2100]],[178]]', () => {
        expect(Max([[[[100,45],[5000],[1789]],[2100]],[178]])).toEqual(5000);
    });

    it('Max of 45', () => {
        expect(Max(45)).toEqual(45);
    });

})