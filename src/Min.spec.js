const Min = require('./Min');

describe('Min', () => {

    it('Min of [2,1,5,4,3]', () => {
        expect(Min([2,1,5,4,3])).toEqual(1);
    });

    it('Min of [[1,2],[78,100],[45,21]]', () => {
        expect(Min([[1,2],[78,100],[45,21]])).toEqual(1);
    });

    it('Min of [[[[100,45],1000,1789],2000],178]', () => {
        expect(Min([[[[100,45],1000,1789],2000],178])).toEqual(45);
    });

    it('Min of [[[[100,45],[5000],[1789]],[2100]],[178]]', () => {
        expect(Min([[[[100,45],[5000],[1789]],[2100]],[178]])).toEqual(45);
    });

    it('Min of 45', () => {
        expect(Min(45)).toEqual(45);
    });

})